package lesson12;

public class Main {

	public static void main(String[] args) {
		// while loop
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		System.out.println("Döngü bitti");
		
		int i = 1;
		
		while (i<10) {
			System.out.println(i);
			i++;
		}

		System.out.println("While döngüsü bitti");
		
		int k = 1;
		
		while (k<10) {
			System.out.println(k);
			k += 2;
		}

		System.out.println("tek sayıların While döngüsü bitti");
		
	}

}
